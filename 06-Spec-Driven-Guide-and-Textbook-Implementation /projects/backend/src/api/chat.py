async def generate_response(
    query: str,
    sources: List[Dict[str, Any]],
    background: Optional[str] = None,
    conversation_history: Optional[List[Message]] = None,
) -> tuple[str, List[Citation]]:
    """
    Generate a response using the LLM.

    Args:
        query: User's question
        sources: Retrieved source chunks
        background: User's background level for personalization
        conversation_history: Previous messages in the conversation

    Returns:
        Tuple of (answer, citations)
    """
    # Format sources for the prompt
    sources_text = "\n\n".join([
        f"[{s.get('chapter_slug', 'unknown')}: {s.get('section_title', 'unknown')}]\n{s.get('chunk_text', '')}"
        for s in sources
    ])

    # Build system prompt with sources
    system_prompt = SYSTEM_PROMPT.format(sources=sources_text)

    # Add personalization
    if background and background in BACKGROUND_PROMPTS:
        system_prompt += f"\n\nPersonalization: {BACKGROUND_PROMPTS[background]}"

    # In production, this would call LiteLLM with Gemini
    # For now, return a placeholder that demonstrates the grounding
    citations = [
        Citation(
            chapter_slug=s.get("chapter_slug", "intro"),
            section_title=s.get("section_title", "Introduction"),
            chunk_text=s.get("chunk_text", "")[:200] + "...",
        )
        for s in sources
    ]

    # Placeholder response - in production, this would be the LLM response
    answer = f"""Based on the textbook content, I can help you with that.

{sources[0].get('chunk_text', 'Content not found')[:500] if sources else 'No relevant sources found.'}

For more details, see the [{sources[0].get('chapter_slug', 'intro')}] chapter.

Note: In production, this response would be generated by Gemini via LiteLLM with the retrieved context."""

    return answer, citations


def handle_chat_unavailable():
    """Handle case when chatbot is unavailable."""
    raise HTTPException(
        status_code=503,
        detail="Chat service temporarily unavailable. Please try again later."
    )


@router.post("/chat", response_model=ChatResponse)
async def chat(request: ChatRequest):
    """
    Send a chat message and receive an answer grounded in textbook content.

    The chatbot answers questions using only information from the textbook,
    always citing sources. Out-of-scope questions are politely redirected.

    Args:
        request: Chat request with user message

    Returns:
        Chat response with answer and citations

    Raises:
        HTTPException: 400 for invalid request, 503 for service unavailable
    """
    try:
        # Validate message
        if not request.message or len(request.message.strip()) == 0:
            raise HTTPException(
                status_code=400,
                detail="Message cannot be empty"
            )

        if len(request.message) > 2000:
            raise HTTPException(
                status_code=400,
                detail="Message too long. Maximum 2000 characters."
            )

        # Retrieve relevant sources
        try:
            sources, citations = await retrieve_for_chat(
                query=request.message,
                max_sources=3,
            )
        except Exception as e:
            print(f"Error retrieving sources: {e}")
            # If retrieval fails, we can't provide grounded answers
            return ChatResponse(
                answer="I'm unable to search the textbook right now. Please try again later.",
                sources=[],
            )

        # Check if we found relevant sources
        if not sources:
            return ChatResponse(
                answer="I don't have information about that in the textbook. Please ask a question related to Agentic AI in DevOps.",
                sources=[],
            )

        # Generate response
        answer, formatted_citations = await generate_response(
            query=request.message,
            sources=sources,
            background=None,  # Would come from user context
        )

        return ChatResponse(
            answer=answer,
            sources=formatted_citations,
        )

    except HTTPException:
        raise
    except Exception as e:
        print(f"Chat error: {e}")
        handle_chat_unavailable()


